package Dasar.ControlStructure.Percabangan;

public class IFstatment {
    public static void main(String[] args) {
        int a = 2; // mendeklarasikan variable a dengan tipe data int

        if (a == 2) { //membuat expresi percabangan if dengan ketentuan jika a sama 'dengan' 2;
        // jika a sama dengan 2; maka lakukan perintah berikut :
            System.out.println("Hello World");
        } else { //merupakan conditional jika data yang dimasukan berbeda dan tidak sama;
            // jika salah maka jalankan perintah berikut
            System.out.println("Semangat Dong");
        }
    }
}

